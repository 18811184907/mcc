#!/usr/bin/env node

/**
 * PostToolUse Hook: Sync PROJECT_VAULT.md → .env.local + .env.example + ~/.ssh/config + SECRETS-INDEX.md
 *
 * Triggered after Write|Edit|MultiEdit when the target file is one of:
 *   - docs/PROJECT_VAULT.md         (current default since 2026-04-28)
 *   - .claude/PROJECT_VAULT.md      (legacy location, still supported)
 * Parses the markdown vault and propagates to standard locations:
 *
 *   - .env.local                    (gitignored, app code uses process.env.X)
 *   - .env.example                  (committed, lists all keys with placeholder values)
 *   - ~/.ssh/config                 (managed block per project, SSH section auto-synced)
 *   - .claude/SECRETS-INDEX.md      (committed, lists field names + descriptions, no values)
 *   - .gitignore                    (auto-add PROJECT_VAULT.md, .env.local, .env.*.local)
 *
 * Cross-platform (Windows/macOS/Linux). Pure regex parsing, zero LLM cost.
 *
 * Vault markdown format:
 *
 *   ## <section name>
 *   - KEY = `value`              # env-style entry
 *   - KEY = value                # backticks optional
 *   - 备注：xxx                  # comments (skipped)
 *
 *   ## SSH / 服务器             # any section matching /SSH|服务器|server/i
 *   - host-name:                 # SSH host block
 *       host = 192.168.1.10
 *       user = deploy
 *       key = ~/.ssh/prod_id_rsa
 *       port = 22
 */

'use strict';

const fs = require('fs');
const path = require('path');
const os = require('os');
const {
  parseVault,
  sanitizeSshField,
  expandHome,
  escapeRegex,
  secureWrite,
} = require('../lib/vault-parser');

const MAX_STDIN = 1024 * 1024;
const WATCHDOG_MS = 5000;

let data = '';
process.stdin.setEncoding('utf8');

const watchdog = setTimeout(() => {
  try { process.stdout.write(data); } catch (_) { /* noop */ }
  process.exit(0);
}, WATCHDOG_MS);
watchdog.unref?.();

process.stdin.on('data', chunk => {
  if (data.length < MAX_STDIN) {
    data += chunk.substring(0, MAX_STDIN - data.length);
  }
});

process.stdin.on('end', () => {
  try { runSync(); } catch (err) {
    process.stderr.write(`[vault-sync] error: ${err.message}\n`);
  }
  clearTimeout(watchdog);
  try { process.stdout.write(data); } catch (_) { /* noop */ }
  process.exit(0);
});

function runSync() {
  const payload = parseStdinPayload(data);
  const filePath = extractTargetFile(payload);
  if (!filePath) return;

  // Only act on PROJECT_VAULT.md (either docs/ or legacy .claude/)
  const normalized = filePath.replace(/\\/g, '/');
  const isDocsVault = /\/docs\/PROJECT_VAULT\.md$/i.test(normalized);
  const isLegacyVault = /\/\.claude\/PROJECT_VAULT\.md$/i.test(normalized);
  if (!isDocsVault && !isLegacyVault) return;

  if (!fs.existsSync(filePath)) return;
  const vaultContent = fs.readFileSync(filePath, 'utf8');

  // Project root = parent of docs/ or .claude/
  const projectRoot = path.dirname(path.dirname(filePath));
  const projectName = path.basename(projectRoot);

  const { env, sshHosts } = parseVault(vaultContent);

  ensureGitignore(projectRoot);
  syncEnvLocal(env, projectRoot);
  syncEnvExample(env, projectRoot);
  syncSecretsIndex(env, sshHosts, projectRoot);
  if (sshHosts.length > 0) syncSshConfig(sshHosts, projectName);

  process.stderr.write(
    `[vault-sync] ✓ Synced ${env.length} env entries`
    + (sshHosts.length ? ` + ${sshHosts.length} SSH hosts` : '')
    + ` from ${path.relative(projectRoot, filePath)}\n`
  );
}

// ─── Sync Targets ───────────────────────────────────────────────────────────
// Parser + sanitizer + helpers all moved to lib/vault-parser.js (shared with
// post-user-vault-sync.js + pre-vault-leak-detect.js so behavior never drifts).
// v2.6.2 NOTE: previously this file had its own parseVault that lacked
// placeholder filtering — `<your-key>` template lines would land in .env.local.
// Using the shared parser fixes that real bug.

function syncEnvLocal(env, projectRoot) {
  const envLocalPath = path.join(projectRoot, '.env.local');
  const lines = [
    '# Auto-generated by MCC vault-sync hook from docs/PROJECT_VAULT.md',
    '# Do not edit manually — your changes will be overwritten.',
    '# To change values, edit docs/PROJECT_VAULT.md and save.',
    '',
  ];
  for (const { key, value } of env) {
    lines.push(`${key}=${value}`);
  }
  // secureWrite: symlink-safe, atomic, Windows ACL lockdown
  secureWrite(envLocalPath, lines.join('\n') + '\n', { mode: 0o600 });
}

function syncEnvExample(env, projectRoot) {
  const examplePath = path.join(projectRoot, '.env.example');

  // Preserve user's manual entries; MCC-managed keys get placeholder
  let preserved = new Map();
  if (fs.existsSync(examplePath)) {
    const existing = fs.readFileSync(examplePath, 'utf8');
    for (const line of existing.split(/\r?\n/)) {
      const m = line.match(/^([A-Z][A-Z0-9_]*)=(.*)$/);
      if (m) preserved.set(m[1], m[2]);
    }
  }

  const managedKeys = new Set(env.map(e => e.key));
  const lines = [
    '# Generated by MCC vault-sync hook from docs/PROJECT_VAULT.md',
    '# This file IS committed. Values shown are placeholders.',
    '# Real values live in .env.local (gitignored) — auto-synced from PROJECT_VAULT.md.',
    '',
  ];
  // MCC-managed keys with placeholder
  for (const { key, section } of env) {
    const placeholder = preserved.get(key) || '';
    lines.push(`# ${section}`);
    lines.push(`${key}=${placeholder}`);
  }
  // User's manual entries (not in vault)
  const userOnly = [...preserved.keys()].filter(k => !managedKeys.has(k));
  if (userOnly.length > 0) {
    lines.push('', '# User-added entries (not in PROJECT_VAULT.md)');
    for (const k of userOnly) {
      lines.push(`${k}=${preserved.get(k)}`);
    }
  }

  // .env.example is committed (not secret) so no Windows ACL lockdown — but
  // still go through secureWrite for symlink defense + atomic replace.
  secureWrite(examplePath, lines.join('\n') + '\n', { mode: 0o644, lockdownWindows: false });
}

function syncSshConfig(sshHosts, projectName) {
  const sshDir = path.join(os.homedir(), '.ssh');
  const sshConfigPath = path.join(sshDir, 'config');
  const startMarker = `# MCC-Managed: ${projectName} START`;
  const endMarker = `# MCC-Managed: ${projectName} END`;

  let existing = '';
  if (fs.existsSync(sshConfigPath)) {
    existing = fs.readFileSync(sshConfigPath, 'utf8');
  }

  // Strip old MCC block for this project (idempotent)
  const blockRegex = new RegExp(
    `\\n*${escapeRegex(startMarker)}[\\s\\S]*?${escapeRegex(endMarker)}\\n*`,
    'g'
  );
  existing = existing.replace(blockRegex, '\n');

  // Build new block.
  // SAFETY: every field is sanitized — values originate from PROJECT_VAULT.md, which a malicious
  // contributor or compromised editor session can mutate. Newlines or null bytes inside a value
  // would otherwise inject arbitrary ssh_config directives (e.g. ProxyJump to attacker host).
  const blockLines = ['', startMarker];
  for (const host of sshHosts) {
    if (!host.name) continue;
    const safeName = sanitizeSshField('name', host.name);
    if (!safeName || !/^[A-Za-z0-9_.\-*?]+$/.test(safeName)) {
      process.stderr.write(`[vault-sync] skipping ssh host with invalid name: ${JSON.stringify(host.name)}\n`);
      continue;
    }
    const safeHost = sanitizeSshField('host', host.host);
    const safeUser = sanitizeSshField('user', host.user);
    const safeKey  = sanitizeSshField('key',  host.key);
    const safePort = sanitizeSshField('port', host.port);
    if (safePort && !/^\d{1,5}$/.test(safePort)) {
      process.stderr.write(`[vault-sync] skipping invalid port "${host.port}" for host ${safeName}\n`);
      continue;
    }
    blockLines.push(`Host ${safeName}`);
    if (safeHost) blockLines.push(`    HostName ${safeHost}`);
    if (safeUser) blockLines.push(`    User ${safeUser}`);
    if (safeKey) {
      const expanded = expandHome(safeKey);
      // Defense in depth: IdentityFile must stay within HOME.
      // Bare /etc/shadow / /var/run/* etc. are refused so a compromised vault
      // can't make SSH read system files when probing keys.
      const resolved = path.resolve(expanded);
      const home = path.resolve(os.homedir());
      if (resolved.includes('..')) {
        process.stderr.write(`[vault-sync] skipping IdentityFile with traversal: ${JSON.stringify(safeKey)}\n`);
      } else if (!resolved.startsWith(home + path.sep) && resolved !== home) {
        process.stderr.write(`[vault-sync] skipping IdentityFile outside HOME: ${JSON.stringify(safeKey)}\n`);
      } else {
        blockLines.push(`    IdentityFile ${expanded}`);
      }
    }
    if (safePort) blockLines.push(`    Port ${safePort}`);
    blockLines.push('');
  }
  blockLines.push(endMarker, '');

  if (!fs.existsSync(sshDir)) {
    fs.mkdirSync(sshDir, { recursive: true, mode: 0o700 });
  }
  // v2.6.4: 走 secureWrite（symlink-safe + atomic + Windows ACL lockdown），
  // 跟 user-vault-sync 的 ssh config 写入对齐。之前直接 fs.writeFileSync 在
  // ~/.ssh/config 这种敏感文件上没有 symlink defense（codex audit 找到的）。
  secureWrite(sshConfigPath, existing.trimEnd() + blockLines.join('\n'), { mode: 0o600 });
}

function syncSecretsIndex(env, sshHosts, projectRoot) {
  const indexPath = path.join(projectRoot, '.claude', 'SECRETS-INDEX.md');
  const lines = [
    '# Secrets Index · safe to commit',
    '',
    '> Auto-generated by MCC from `docs/PROJECT_VAULT.md` (or legacy `.claude/PROJECT_VAULT.md`).',
    '> This file lists secret **field names** only — values stay in `PROJECT_VAULT.md` (gitignored).',
    '> Use this to onboard teammates: they see what secrets exist, then ask you for the values.',
    '',
  ];

  // Group env by section
  const bySection = new Map();
  for (const { key, section } of env) {
    if (!bySection.has(section)) bySection.set(section, []);
    bySection.get(section).push(key);
  }

  for (const [section, keys] of bySection) {
    lines.push(`## ${section}`, '');
    for (const k of keys) lines.push(`- **${k}**`);
    lines.push('');
  }

  if (sshHosts.length > 0) {
    lines.push('## SSH Hosts', '');
    for (const h of sshHosts) {
      const meta = [];
      if (h.user) meta.push(h.user);
      if (h.host) meta.push(h.host);
      const suffix = meta.length ? ` (${meta.join('@')})` : '';
      lines.push(`- **${h.name}**${suffix}`);
    }
    lines.push('');
  }

  // v2.6.4: SECRETS-INDEX.md is committed (no values), so no Windows ACL
  // lockdown — but secureWrite still gives us symlink defense + atomic replace.
  secureWrite(indexPath, lines.join('\n'), { mode: 0o644, lockdownWindows: false });
}

// ─── .gitignore Guard ───────────────────────────────────────────────────────

function ensureGitignore(projectRoot) {
  const gitignorePath = path.join(projectRoot, '.gitignore');
  const required = [
    'docs/PROJECT_VAULT.md',
    '.claude/PROJECT_VAULT.md',  // legacy — kept for backward-compat
    '.env.local',
    '.env.*.local',
    '.deploy.local.md',
  ];

  let existing = '';
  if (fs.existsSync(gitignorePath)) {
    existing = fs.readFileSync(gitignorePath, 'utf8');
  }

  const existingLines = new Set(
    existing.split(/\r?\n/).map(l => l.trim()).filter(Boolean)
  );
  const missing = required.filter(p => !existingLines.has(p));

  if (missing.length === 0) return false;

  const additions = [
    '',
    '# MCC PROJECT_VAULT (auto-managed — secrets must never enter git)',
    ...missing,
    '',
  ];
  fs.writeFileSync(gitignorePath, existing.trimEnd() + '\n' + additions.join('\n'));
  return true;
}

// ─── Helpers ────────────────────────────────────────────────────────────────

function parseStdinPayload(raw) {
  if (!raw || !raw.trim()) return {};
  try { return JSON.parse(raw); } catch { return {}; }
}

function extractTargetFile(payload) {
  // Claude Code passes tool_input in the hook payload
  const input = payload?.tool_input || payload?.toolInput || {};
  return input.file_path || input.filePath || input.path || null;
}

